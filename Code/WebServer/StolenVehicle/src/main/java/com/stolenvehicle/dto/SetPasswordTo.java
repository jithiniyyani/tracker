package com.stolenvehicle.dto;

public class SetPasswordTo {

	private String activiationId;

	private String emailAddress;

	private String password;

	public String getActiviationId() {
		return activiationId;
	}

	public void setActiviationId(String activiationId) {
		this.activiationId = activiationId;
	}

	public String getEmailAddress() {
		return emailAddress;
	}

	public void setEmailAddress(String emailAddress) {
		this.emailAddress = emailAddress;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

}
